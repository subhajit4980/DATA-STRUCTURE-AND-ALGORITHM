#include<bits/stdc++.h>
using namespace std;
struct node {
    int u;
    int v;
    int wt; 
    node(int first, int second, int weight) {
        u = first;
        v = second;
        wt = weight;
    }
};

int main(){
    int N,m;
    cin >> N >> m;
    vector<node> edges; 
    for(int i = 0;i<m;i++) {
        int u, v, wt;
        cin >> u >> v >> wt; 
        edges.push_back(node(u, v, wt)); 
    }

    int src;
    cin >> src; 


    int inf = 10000000; 
    vector<int> dist(N, inf); 

    dist[src] = 0; 
/*Like other Dynamic Programming Problems, the algorithm calculates 
shortest paths in a bottom-up manner. It first calculates the shortest
 distances which have at-most one edge in the path. Then, it calculates the 
 shortest paths with at-most 2 edges, and so on. After the i-th iteration 
 of the outer loop, the shortest paths with at most i edges are calculated. 
 There can be maximum |V| – 1 edges in any simple path, that is why the outer 
 loop runs |v| – 1 times. The idea is, assuming that there is no negative 
 weight cycle, if we have calculated shortest paths with at most i edges, 
 then an iteration over all edges guarantees to give shortest path with 
 at-most (i+1) edge*/
    for(int i = 1;i<=N-1;i++) {
        for(auto it: edges) {
            if(dist[it.u] + it.wt < dist[it.v]) {
                dist[it.v] = dist[it.u] + it.wt; 
            }
        }
    }

    int fl = 0; 
    for(auto it: edges) {
        if(dist[it.u] + it.wt < dist[it.v]) {
            cout << "Negative Cycle"; 
            fl = 1; 
            break; 
        }
    }

    if(!fl) {
        for(int i = 0;i<N;i++) {
            cout << i << " " << dist[i] << endl;
        }
    }


    return 0;
}

